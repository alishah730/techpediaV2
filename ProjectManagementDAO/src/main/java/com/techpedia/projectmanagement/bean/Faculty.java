package com.techpedia.projectmanagement.bean;

public class Faculty {
	
	private long rgstrId;
	
	private String facultyName;
	
	private String facultyDesignation;
	
	private String facultyBranch;

	
	/**
	 * @return the rgstrId
	 */
	public long getRgstrId() {
		return rgstrId;
	}

	/**
	 * @param rgstrId the rgstrId to set
	 */
	public void setRgstrId(long rgstrId) {
		this.rgstrId = rgstrId;
	}

	/**
	 * @return the facultyName
	 */
	public String getFacultyName() {
		return facultyName;
	}

	/**
	 * @param facultyName the facultyName to set
	 */
	public void setFacultyName(String facultyName) {
		this.facultyName = facultyName;
	}

	/**
	 * @return the facultyDesignation
	 */
	public String getFacultyDesignation() {
		return facultyDesignation;
	}

	/**
	 * @param facultyDesignation the facultyDesignation to set
	 */
	public void setFacultyDesignation(String facultyDesignation) {
		this.facultyDesignation = facultyDesignation;
	}

	/**
	 * @return the facultyBranch
	 */
	public String getFacultyBranch() {
		return facultyBranch;
	}

	/**
	 * @param facultyBranch the facultyBranch to set
	 */
	public void setFacultyBranch(String facultyBranch) {
		this.facultyBranch = facultyBranch;
	}

}
